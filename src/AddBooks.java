    private void b1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b1ActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost/library";
        String user = "root";
        String password = "";
        String query = "INSERT INTO books VALUES (?, ?, ?, ?, ?)";
        String id = t1.getText();
        String category = t2.getText();
        String title = t3.getText();
        String author = t4.getText();
        int copies = Integer.parseInt(t5.getText());
        String checkQuery = "UPDATE books SET copies=" + copies + 
                " WHERE title='" + title + "' and category='" + category +
                "' and author='" + author + "';";
        
        try {
            Connection conn = DriverManager.getConnection(url, user, password);
            Statement stm = conn.createStatement();
            int rows = stm.executeUpdate(checkQuery);
            
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Record added successfully");
            } else {
                PreparedStatement pstm = conn.prepareCall(query);
                pstm.setString(1, id);
                pstm.setString(2, category);
                pstm.setString(3, title);
                pstm.setString(4, author);
                pstm.setInt(5, copies);
                pstm.execute();
                JOptionPane.showMessageDialog(this, "Record added successfully");
            }
            t1.setText(null);
            t2.setText(null);
            t3.setText(null);
            t4.setText(null);
            t5.setText(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }   
    }//GEN-LAST:event_b1ActionPerformed

